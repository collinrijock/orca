#!/usr/bin/env node
import { readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.join(__dirname, '..', '..')
const DEFAULT_SOURCE = 'https://onorca.dev/whats-new/changelog.json'
const DEFAULT_OUTPUT = path.join(ROOT, 'src', 'shared', 'marketing-feature-tips.ts')
const DEFAULT_MAX_TIPS = 1

function readArg(name) {
  const index = process.argv.indexOf(name)
  return index === -1 ? null : (process.argv[index + 1] ?? null)
}

function parsePositiveInteger(value, fallback) {
  if (!value) {
    return fallback
  }
  const parsed = Number(value)
  return Number.isInteger(parsed) && parsed > 0 ? parsed : fallback
}

function toAbsolutePath(value) {
  return path.isAbsolute(value) ? value : path.join(process.cwd(), value)
}

async function readJsonSource(source) {
  if (/^https?:\/\//.test(source)) {
    const response = await fetch(source)
    if (!response.ok) {
      throw new Error(`Failed to fetch ${source}: ${response.status} ${response.statusText}`)
    }
    return response.json()
  }

  return JSON.parse(await readFile(toAbsolutePath(source), 'utf8'))
}

function asciiCopy(value) {
  return value
    .normalize('NFKC')
    .replace(/[\u2018\u2019]/g, "'")
    .replace(/[\u201C\u201D]/g, '"')
    .replace(/[\u2013\u2014]/g, '--')
    .replace(/\u2026/g, '...')
}

function toFeatureTipId(version) {
  const normalized = version
    .trim()
    .toLowerCase()
    .replace(/^v/, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')

  if (!normalized) {
    throw new Error(`Cannot build feature tip id from version: ${version}`)
  }
  return `changelog-${normalized}`
}

function asNonEmptyString(value, fieldName) {
  if (typeof value !== 'string' || value.trim() === '') {
    throw new Error(`Changelog entry is missing ${fieldName}`)
  }
  return asciiCopy(value.trim())
}

function toMarketingFeatureTip(entry) {
  const version = asNonEmptyString(entry.version, 'version')
  if (typeof entry.mediaUrl !== 'string' || entry.mediaUrl.trim() === '') {
    return null
  }
  const mediaUrl = asciiCopy(entry.mediaUrl.trim())
  if (!mediaUrl.toLowerCase().endsWith('.gif')) {
    return null
  }

  return {
    id: toFeatureTipId(version),
    priority: 'new',
    eyebrow: `New in ${version}`,
    title: asNonEmptyString(entry.title, 'title'),
    description: asNonEmptyString(entry.description, 'description'),
    action: 'open-release-notes',
    ctaLabel: "See what's new",
    mediaUrl,
    releaseNotesUrl: asNonEmptyString(entry.releaseNotesUrl, 'releaseNotesUrl')
  }
}

function tsString(value) {
  return `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`
}

function serializeTip(tip) {
  return `  {
    id: ${tsString(tip.id)},
    priority: ${tsString(tip.priority)},
    eyebrow: ${tsString(tip.eyebrow)},
    title: ${tsString(tip.title)},
    description:
      ${tsString(tip.description)},
    action: ${tsString(tip.action)},
    ctaLabel: ${tsString(tip.ctaLabel)},
    mediaUrl: ${tsString(tip.mediaUrl)},
    releaseNotesUrl: ${tsString(tip.releaseNotesUrl)}
  }`
}

function serializeFeatureTips(tips, source) {
  const body = tips.map(serializeTip).join(',\n')
  return `// Generated by config/scripts/sync-feature-tips-from-changelog.mjs.
// Source: ${source}
// Review copy before merging; the workflow only drafts PRs for human approval.
export const MARKETING_FEATURE_TIPS = [
${body}
] as const
`
}

const source = readArg('--source') ?? process.env.FEATURE_TIPS_CHANGELOG_SOURCE ?? DEFAULT_SOURCE
const output =
  readArg('--output') ??
  process.env.FEATURE_TIPS_OUTPUT ??
  path.relative(process.cwd(), DEFAULT_OUTPUT)
const maxTips = parsePositiveInteger(
  readArg('--max-tips') ?? process.env.FEATURE_TIPS_MAX,
  DEFAULT_MAX_TIPS
)

const changelog = await readJsonSource(source)
if (!Array.isArray(changelog)) {
  throw new Error('Changelog source must be a JSON array')
}

const tips = changelog
  .map(toMarketingFeatureTip)
  .filter((tip) => tip !== null)
  .slice(0, maxTips)

if (tips.length === 0) {
  throw new Error('No GIF-backed changelog entries found for feature tips')
}

await writeFile(toAbsolutePath(output), serializeFeatureTips(tips, source))
console.log(`Wrote ${tips.length} marketing feature tip(s) to ${output}`)
